import UIKit

extension UIColor {
   
    // Background Colors
    static let backgroundGray = UIColor(named: "customBackground")
    static let backgroundWhite = UIColor(named: "customWhite")
   
    // Text Colors
    static let textPrimary = UIColor(named: "customBlack") ?? UIColor.black
    
    // Border Colors
    static let borderGray = UIColor(named: "borderColor")
    static let borderBlue = UIColor(named: "customBlue")
}
